package main

import "fmt"

func main() {
	// 1. assign variables with short declaration operator
	x := 42
	y := "James Bond"
	z := true

	// 2. print values stored in those variables
	// a. single print statement
	fmt.Println(x, y, z)

	// b. multiple print statement
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
